function toProgram([name, college, minRate, formula, gender, degree, sourceExcerpt]) {
  return {
    name,
    college,
    min_rate: minRate,
    formula,
    gender,
    degree,
    source_excerpt: sourceExcerpt,
  };
}

const universities = JSON.parse(readFileSync(dataPath, "utf8"));
for (const university of universities) {
  const programs = manualPrograms[university.id];
  if (!programs) continue;
  university.programs = programs.map(toProgram);
  university.extraction_method = "manual_docx";
  university.manual_source = `${university.name}.docx`;
}

writeFileSync(dataPath, JSON.stringify(universities, null, 2) + "\n", "utf8");

try {
  const report = JSON.parse(readFileSync(reportPath, "utf8"));
  for (const item of report) {
    const programs = manualPrograms[item.id];
    if (!programs) continue;
    item.extraction_method = "manual_docx";
    item.programs_count = programs.length;
    item.extraction_notes = "تمت تعبئة البرامج يدوياً من ملف DOCX مستخرج من المصدر.";
  }
  writeFileSync(reportPath, JSON.stringify(report, null, 2) + "\n", "utf8");
} catch {
  // Report is auxiliary; keep the main data update even if it is unavailable.
}

